require_once($CFG->dirroot . '/mod/groupquiz/backup/moodle2/restore_groupquiz_stepslib.php'); // Because it exists (must)

/**
 * groupquiz restore task that provides all the settings and steps to perform one
 * complete restore of the activity
 */
class restore_groupquiz_activity_task extends restore_activity_task {

    /**
     * Define (add) particular settings this activity can have
     */
    protected function define_my_settings() {
        // No particular settings for this activity
    }

    /**
     * Define (add) particular steps this activity can have
     */
    protected function define_my_steps() {
        // label only has one structure step
        $this->add_step(new restore_groupquiz_activity_structure_step('groupquiz_structure', 'groupquiz.xml'));
    }

    /**
     * Define the contents in the activity that must be
     * processed by the link decoder
     */
    static public function define_decode_contents() {
        $contents = array();

        $contents[] = new restore_decode_content('groupquiz', array('intro'), 'groupquiz');

        return $contents;
    }

    /**
     * Define the decoding rules for links belonging
     * to the activity to be executed by the link decoder
     */
    static public function define_decode_rules() {
        $rules = array();

        $rules[] = new restore_decode_rule('GROUPQUIZVIEWBYID', '/mod/groupquiz/view.php?id=$1', 'course_module');
        $rules[] = new restore_decode_rule('GROUPQUIZINDEX', '/mod/groupquiz/index.php?id=$1', 'course');

        return $rules;

    }

    /**
     * Define the restore log rules that will be applied
     * by the {@link restore_logs_processor} when restoring
     * groupquiz logs. It must return one array
     * of {@link restore_log_rule} objects
     */
    static public function define_restore_log_rules() {
        $rules = array();

        $rules[] = new restore_log_rule('groupquiz', 'add', 'view.php?id={course_module}', '{groupquiz}');
        $rules[] = new restore_log_rule('groupquiz', 'update', 'view.php?id={course_module}', '{groupquiz}');
        $rules[] = new restore_log_rule('groupquiz', 'view', 'view.php?id={course_module}', '{groupquiz}');
	//TODO add all other events such as event start, event end, review page viewed, etc
        return $rules;
    }

    /**
     * Define the restore log rules that will be applied
     * by the {@link restore_logs_processor} when restoring
     * course logs. It must return one array
     * of {@link restore_log_rule} objects
     *
     * Note this rules are applied when restoring course logs
     * by the restore final task, but are defined here at
     * activity level. All them are rules not linked to any module instance (cmid = 0)
     */
    static public function define_restore_log_rules_for_course() {
        $rules = array();

        $rules[] = new restore_log_rule('groupquiz', 'view all', 'index.php?id={course}', null);

        return $rules;
    }
}
